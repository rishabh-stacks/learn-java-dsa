public class Conditionals {
    public static void main(String[] args) {

        // Syntax of IF-Statement
        /*
            if (Boolean T or F) {
            --- Body 1
            }
            else {
            --- Body 2
            }
         */

        int salary = 25400;
        if (salary > 10000) {
            salary = salary + 2000;
        }
        else {
            salary = salary + 1000;
        }

        System.out.println(salary);


        // Syntax of Else-If Statement
        /*
            if (Boolean T or F) {
            --- Body 1
            }
            elseif (Boolean T or F) {
            --- Body 2
            }
            |
            |
            |
            else {
            --- Body 3
            }
         */
        

    }
}
